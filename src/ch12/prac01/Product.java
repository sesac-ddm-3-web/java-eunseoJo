package ch12.prac01;


// 상품 정보를 저장하는 클래스
class Product {
    String id;
    String name;
    int price;
    int stock;

    public Product(String id, String name, int price, int stock) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.stock = stock;
    }
}


